/**
 * 쿼리 신선도(staleTime) 정책 상수.
 *
 * 새 쿼리를 추가할 때 아래 기준으로 staleTime을 선택한다.
 * 전역 기본값은 FREQUENT(30초)이며, 각 쿼리에서 개별 재정의가 필요한 경우에만 명시한다.
 *
 * | 티어       | 값      | 사용 예                                   |
 * |------------|---------|-------------------------------------------|
 * | REALTIME   | 0ms     | 발송 상태, 파이프라인 상태 (항상 재페치)  |
 * | FREQUENT   | 30s     | 기사 목록, 통계 (기본값)                  |
 * | MODERATE   | 2min    | 카테고리 목록, 설정                       |
 * | STABLE     | 5min    | 프리셋, 사용자 프로필                     |
 */
export const STALE_TIMES = {
  /** 실시간 데이터 (발송 상태, 파이프라인 상태): 항상 재페치 */
  REALTIME: 0,
  /** 자주 바뀌는 데이터 (기사 목록, 통계): 30초 */
  FREQUENT: 30_000,
  /** 중간 빈도 데이터 (카테고리 목록, 설정): 2분 */
  MODERATE: 2 * 60_000,
  /** 거의 안 바뀌는 데이터 (프리셋, 사용자 프로필): 5분 */
  STABLE: 5 * 60_000,
} as const;
